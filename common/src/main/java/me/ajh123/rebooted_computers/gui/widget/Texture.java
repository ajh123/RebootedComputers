package me.ajh123.rebooted_computers.gui.widget;

import me.ajh123.rebooted_computers.RebootedComputers;
import net.minecraft.resources.ResourceLocation;

public final class Texture {
    public final ResourceLocation location;
    public final int width, height;

    public Texture(final String location, final int width, final int height) {
        this(ResourceLocation.fromNamespaceAndPath(RebootedComputers.MOD_ID, location), width, height);
    }

    public Texture(final ResourceLocation location, final int width, final int height) {
        this.location = location;
        this.width = width;
        this.height = height;
    }
}
